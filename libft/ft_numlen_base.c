#include "includes/libft.h"

int		ft_numlen_base(long long value, int base)
{
	unsigned long long	num;

	num = (value < 0 && base == 10) ? -value : value;
	return (ft_unumlen_base(num, base) + (value < 0 && base == 10));
}
